import mongoose from 'mongoose';

const deviceTokenSchema = new mongoose.Schema(
  {
    token: {
      type: String,
      required: true,
      unique: true,
    },
    userId: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'User',
    },
  },
  {
    timestamps: true,
  }
);

const DeviceToken = mongoose.model('DeviceToken', deviceTokenSchema);
export default DeviceToken;